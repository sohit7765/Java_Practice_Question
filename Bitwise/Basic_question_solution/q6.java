
public class q6 {

	public static void main(String[] args) {
		String s1="hello";
		String s2="world";
		String temp="";
		System.out.println("before Swapping the given String");
		System.out.println(s1 +" " + s2);
		temp=s1;
		s1=s2;
		s2=temp;
		System.out.println("After Swapping the given String ");
		System.out.println(s1 +" "+ s2);

	}

}
